package com.BookingTaxi;
public class Places {
    
    int id;
    String placeName;

    public Places(int id, String placeName) {
        this.id = id;
        this.placeName = placeName;
    }

    public int getId() {
        return id;
    }

    public String getPlaceName() {
        return placeName;
    }
    
    
}
